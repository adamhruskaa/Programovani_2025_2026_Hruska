public class Hrana {
    private final Vrchol vrcholA;
    private final Vrchol vrcholB;

    public Hrana(Vrchol vrcholA, Vrchol vrcholB) {
        this.vrcholA = vrcholA;
        this.vrcholB = vrcholB;
    }

    public Vrchol getVrcholA() {
        return vrcholA;
    }
    public Vrchol getVrcholB() {
        return vrcholB;
    }
}
